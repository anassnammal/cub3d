#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned char	*s;
	unsigned char	*d;

	s = (unsigned char *)src;
	d = (unsigned char *)dst;
	if (s != 0 || d != 0)
	{
		while (n-- > 0)
			*d++ = *s++;
	}
	return (dst);
}
